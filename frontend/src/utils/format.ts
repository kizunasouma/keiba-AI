/**
 * 半角カナ→全角カナ変換
 * JV-Linkのデータは半角カナで格納されているため、表示時に全角に変換する
 */
const HALF_TO_FULL: Record<string, string> = {
  'ｱ':'ア','ｲ':'イ','ｳ':'ウ','ｴ':'エ','ｵ':'オ',
  'ｶ':'カ','ｷ':'キ','ｸ':'ク','ｹ':'ケ','ｺ':'コ',
  'ｻ':'サ','ｼ':'シ','ｽ':'ス','ｾ':'セ','ｿ':'ソ',
  'ﾀ':'タ','ﾁ':'チ','ﾂ':'ツ','ﾃ':'テ','ﾄ':'ト',
  'ﾅ':'ナ','ﾆ':'ニ','ﾇ':'ヌ','ﾈ':'ネ','ﾉ':'ノ',
  'ﾊ':'ハ','ﾋ':'ヒ','ﾌ':'フ','ﾍ':'ヘ','ﾎ':'ホ',
  'ﾏ':'マ','ﾐ':'ミ','ﾑ':'ム','ﾒ':'メ','ﾓ':'モ',
  'ﾔ':'ヤ','ﾕ':'ユ','ﾖ':'ヨ',
  'ﾗ':'ラ','ﾘ':'リ','ﾙ':'ル','ﾚ':'レ','ﾛ':'ロ',
  'ﾜ':'ワ','ﾝ':'ン','ｦ':'ヲ',
  'ｧ':'ァ','ｨ':'ィ','ｩ':'ゥ','ｪ':'ェ','ｫ':'ォ',
  'ｬ':'ャ','ｭ':'ュ','ｮ':'ョ','ｯ':'ッ',
  'ﾞ':'゛','ﾟ':'゜','ｰ':'ー','･':'・','｢':'「','｣':'」','｡':'。','､':'、',
  '　':' ',
}

// 濁点・半濁点の結合
const DAKUTEN: Record<string, string> = {
  'カ゛':'ガ','キ゛':'ギ','ク゛':'グ','ケ゛':'ゲ','コ゛':'ゴ',
  'サ゛':'ザ','シ゛':'ジ','ス゛':'ズ','セ゛':'ゼ','ソ゛':'ゾ',
  'タ゛':'ダ','チ゛':'ヂ','ツ゛':'ヅ','テ゛':'デ','ト゛':'ド',
  'ハ゛':'バ','ヒ゛':'ビ','フ゛':'ブ','ヘ゛':'ベ','ホ゛':'ボ',
  'ウ゛':'ヴ',
  'ハ゜':'パ','ヒ゜':'ピ','フ゜':'プ','ヘ゜':'ペ','ホ゜':'ポ',
}

export function toFullWidth(s: string | null | undefined): string {
  if (!s) return ''
  // 1. 半角→全角変換
  let result = ''
  for (const c of s) {
    result += HALF_TO_FULL[c] ?? c
  }
  // 2. 濁点・半濁点の結合
  for (const [k, v] of Object.entries(DAKUTEN)) {
    result = result.replaceAll(k, v)
  }
  return result.trim()
}
