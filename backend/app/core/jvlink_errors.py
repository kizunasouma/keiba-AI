def get_jvlink_error_message(error_type: str, code: int) -> tuple[str, str]:
    """
    JV-Linkエラーコードから日本語メッセージと対処方法を返す。
    Returns: (エラー名, 対処方法)
    """
    error_maps = {
        "JVInit": JVINIT_ERRORS,
        "JVSetServiceKey": JVSETSERVICEKEY_ERRORS,
        "JVOpen": JVOPEN_ERRORS,
    }
    error_map = error_maps.get(error_type, {})
    if code in error_map:
        return error_map[code]
    return (f"不明なエラー（コード: {code}）", "サポートにお問い合わせください。")
